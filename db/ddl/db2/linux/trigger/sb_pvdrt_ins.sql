		x.LST_UPD_ID,
		x.LST_UPD_TS,
		x.ZIP_SFX_NO,
		x.EDUCATION,
		x.EMPL_STAT,
		x.PRIM_INC,
		x.SEC_INC,
		x.YR_INC_AMT,
		x.HISP_CD,
		x.MRTL_STC,
