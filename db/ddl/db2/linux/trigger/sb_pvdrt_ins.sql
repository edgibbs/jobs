FOR EACH ROW MODE DB2SQL
BEGIN ATOMIC
	MERGE INTO CWSRS1.SB_PVDRT tgt USING ( 
		SELECT
		nrow.IDENTIFIER,
		nrow.ADD_TEL_NO,
		nrow.ADD_EXT_NO,
		nrow.BIRTH_DT,
		nrow.CA_DLIC_NO,
