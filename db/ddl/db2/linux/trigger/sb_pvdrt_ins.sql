		nrow.LIS_PER_ID,
		nrow.EMAIL_ADDR,
		nrow.ETH_UD_CD,
		nrow.HISP_UD_CD,
		nrow.RESOST_IND,
		nrow.PASSBC_CD
		FROM sysibm.sysdummy1
	) X ON (tgt.IDENTIFIER = X.IDENTIFIER)
	WHEN MATCHED THEN UPDATE SET 
		ADD_TEL_NO = x.ADD_TEL_NO,
		ADD_EXT_NO = x.ADD_EXT_NO,
		BIRTH_DT = x.BIRTH_DT,
		CA_DLIC_NO = x.CA_DLIC_NO,
		CITY_NM = x.CITY_NM,
		EMPLYR_NM = x.EMPLYR_NM,
		FIRST_NM = x.FIRST_NM,
		FRG_ADRT_B = x.FRG_ADRT_B,
		GENDER_IND = x.GENDER_IND,
		IND_TRBC = x.IND_TRBC,
		LAST_NM = x.LAST_NM,
		MID_INI_NM = x.MID_INI_NM,
		NMPRFX_DSC = x.NMPRFX_DSC,
		SS_NO = x.SS_NO,
		STATE_C = x.STATE_C,
		STREET_NM = x.STREET_NM,
		STREET_NO = x.STREET_NO,
		SUFX_TLDSC = x.SUFX_TLDSC,
		ZIP_NO = x.ZIP_NO,
		LST_UPD_ID = x.LST_UPD_ID,
		LST_UPD_TS = x.LST_UPD_TS,
		ZIP_SFX_NO = x.ZIP_SFX_NO,
		EDUCATION = x.EDUCATION,
		EMPL_STAT = x.EMPL_STAT,
		PRIM_INC = x.PRIM_INC,
		SEC_INC = x.SEC_INC,
		YR_INC_AMT = x.YR_INC_AMT,
		HISP_CD = x.HISP_CD,
		MRTL_STC = x.MRTL_STC,
		LISOWNIND = x.LISOWNIND,
		LIS_PER_ID = x.LIS_PER_ID,
		EMAIL_ADDR = x.EMAIL_ADDR,
		ETH_UD_CD = x.ETH_UD_CD,
		HISP_UD_CD = x.HISP_UD_CD,
		RESOST_IND = x.RESOST_IND,
		PASSBC_CD = x.PASSBC_CD,
		IBMSNAP_OPERATION = 'I',
		IBMSNAP_LOGMARKER = current timestamp
	WHEN NOT MATCHED THEN INSERT (
		IDENTIFIER,
		ADD_TEL_NO,
		ADD_EXT_NO,
		BIRTH_DT,
		CA_DLIC_NO,
		CITY_NM,
		EMPLYR_NM,
		FIRST_NM,
		FRG_ADRT_B,
		GENDER_IND,
		IND_TRBC,
		LAST_NM,
		MID_INI_NM,
		NMPRFX_DSC,
		SS_NO,
		STATE_C,
		STREET_NM,
		STREET_NO,
		SUFX_TLDSC,
		ZIP_NO,
		LST_UPD_ID,
		LST_UPD_TS,
		ZIP_SFX_NO,
		EDUCATION,
		EMPL_STAT,
		PRIM_INC,
		SEC_INC,
		YR_INC_AMT,
		HISP_CD,
		MRTL_STC,
		LISOWNIND,
		LIS_PER_ID,
		EMAIL_ADDR,
		ETH_UD_CD,
