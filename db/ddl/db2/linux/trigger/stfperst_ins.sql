DROP TRIGGER CWSINT.trg_stfperst_ins;

CREATE TRIGGER CWSINT.trg_stfperst_ins
AFTER INSERT ON CWSINT.STFPERST
REFERENCING NEW AS NROW
FOR EACH ROW MODE DB2SQL
BEGIN ATOMIC
	MERGE INTO CWSRS1.STFPERST tgt USING ( 
		SELECT
		nrow.IDENTIFIER,
		nrow.END_DT,
		nrow.FIRST_NM,
		nrow.JOB_TL_DSC,
		nrow.LAST_NM,
		nrow.MID_INI_NM,
		nrow.NMPRFX_DSC,
		nrow.PHONE_NO,
		nrow.TEL_EXT_NO,
		nrow.START_DT,
		nrow.SUFX_TLDSC,
		nrow.TLCMTR_IND,
		nrow.LST_UPD_ID,
		nrow.LST_UPD_TS,
		nrow.FKCWS_OFFT,
		nrow.AVLOC_DSC,
		nrow.SSRS_WKRID,
		nrow.CNTY_SPFCD,
		nrow.DTYWKR_IND,
		nrow.FKCWSADDRT,
		nrow.EMAIL_ADDR
		FROM sysibm.sysdummy1
	) X ON (tgt.IDENTIFIER = X.IDENTIFIER)
	WHEN MATCHED THEN UPDATE SET 
		IDENTIFIER = x.IDENTIFIER,
		END_DT = x.END_DT,
		FIRST_NM = x.FIRST_NM,
		JOB_TL_DSC = x.JOB_TL_DSC,
		LAST_NM = x.LAST_NM,
		MID_INI_NM = x.MID_INI_NM,
		NMPRFX_DSC = x.NMPRFX_DSC,
		PHONE_NO = x.PHONE_NO,
		TEL_EXT_NO = x.TEL_EXT_NO,
		START_DT = x.START_DT,
		SUFX_TLDSC = x.SUFX_TLDSC,
		TLCMTR_IND = x.TLCMTR_IND,
		LST_UPD_ID = x.LST_UPD_ID,
		LST_UPD_TS = x.LST_UPD_TS,
		FKCWS_OFFT = x.FKCWS_OFFT,
		AVLOC_DSC = x.AVLOC_DSC,
		SSRS_WKRID = x.SSRS_WKRID,
		CNTY_SPFCD = x.CNTY_SPFCD,
		DTYWKR_IND = x.DTYWKR_IND,
		FKCWSADDRT = x.FKCWSADDRT,
		EMAIL_ADDR = x.EMAIL_ADDR,
		IBMSNAP_OPERATION = 'I',
		IBMSNAP_LOGMARKER = current timestamp
	WHEN NOT MATCHED THEN INSERT (
		IDENTIFIER,
		END_DT,
		FIRST_NM,
		JOB_TL_DSC,
		LAST_NM,
		MID_INI_NM,
		NMPRFX_DSC,
		PHONE_NO,
		TEL_EXT_NO,
		START_DT,
		SUFX_TLDSC,
		TLCMTR_IND,
		LST_UPD_ID,
		LST_UPD_TS,
		FKCWS_OFFT,
		AVLOC_DSC,
		SSRS_WKRID,
		CNTY_SPFCD,
		DTYWKR_IND,
		FKCWSADDRT,
		EMAIL_ADDR,
		IBMSNAP_OPERATION,
		IBMSNAP_LOGMARKER
	) VALUES (
		x.IDENTIFIER,
		x.END_DT,
		x.FIRST_NM,
		x.JOB_TL_DSC,
		x.LAST_NM,
		x.MID_INI_NM,
		x.NMPRFX_DSC,
		x.PHONE_NO,
		x.TEL_EXT_NO,
		x.START_DT,
		x.SUFX_TLDSC,
		x.TLCMTR_IND,
		x.LST_UPD_ID,
		x.LST_UPD_TS,
		x.FKCWS_OFFT,
		x.AVLOC_DSC,
		x.SSRS_WKRID,
		x.CNTY_SPFCD,
		x.DTYWKR_IND,
		x.FKCWSADDRT,
		x.EMAIL_ADDR,
		'I',
		current timestamp
	);
END
