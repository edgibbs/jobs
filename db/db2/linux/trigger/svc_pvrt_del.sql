DROP TRIGGER CWSINT.trg_svc_pvrt_del;

CREATE TRIGGER CWSINT.trg_svc_pvrt_del
AFTER DELETE ON CWSINT.SVC_PVRT
REFERENCING OLD AS OROW
FOR EACH ROW MODE DB2SQL
BEGIN ATOMIC
	MERGE INTO CWSRS1.SVC_PVRT tgt USING ( 
		SELECT
		orow.IDENTIFIER,
		orow.AGENCY_NM,
		orow.CITY_NM,
		orow.FAX_NO,
		orow.FIRST_NM,
		orow.LAST_NM,
		orow.NMPRFX_DSC,
		orow.PHONE_NO,
		orow.TEL_EXT_NO,
		orow.PSTITL_DSC,
		orow.SVCPVDRC,
		orow.STATE_C,
		orow.STREET_NM,
		orow.STREET_NO,
		orow.SUFX_TLDSC,
		orow.ZIP_NM,
		orow.LST_UPD_ID,
		orow.LST_UPD_TS,
		orow.ZIP_SFX_NO,
		orow.ARCASS_IND,
		orow.EMAIL_ADDR
		FROM sysibm.sysdummy1
	) X ON (tgt.IDENTIFIER = X.IDENTIFIER)
	WHEN MATCHED THEN UPDATE SET 
		AGENCY_NM = x.AGENCY_NM,
		CITY_NM = x.CITY_NM,
		FAX_NO = x.FAX_NO,
		FIRST_NM = x.FIRST_NM,
		LAST_NM = x.LAST_NM,
		NMPRFX_DSC = x.NMPRFX_DSC,
		PHONE_NO = x.PHONE_NO,
		TEL_EXT_NO = x.TEL_EXT_NO,
		PSTITL_DSC = x.PSTITL_DSC,
		SVCPVDRC = x.SVCPVDRC,
		STATE_C = x.STATE_C,
		STREET_NM = x.STREET_NM,
		STREET_NO = x.STREET_NO,
		SUFX_TLDSC = x.SUFX_TLDSC,
		ZIP_NM = x.ZIP_NM,
		LST_UPD_ID = x.LST_UPD_ID,
		LST_UPD_TS = x.LST_UPD_TS,
		ZIP_SFX_NO = x.ZIP_SFX_NO,
		ARCASS_IND = x.ARCASS_IND,
		EMAIL_ADDR = x.EMAIL_ADDR,
		IBMSNAP_OPERATION = 'D',
		IBMSNAP_LOGMARKER = current timestamp
	WHEN NOT MATCHED THEN INSERT (
		IDENTIFIER,
		AGENCY_NM,
		CITY_NM,
		FAX_NO,
		FIRST_NM,
		LAST_NM,
		NMPRFX_DSC,
		PHONE_NO,
		TEL_EXT_NO,
		PSTITL_DSC,
		SVCPVDRC,
		STATE_C,
		STREET_NM,
		STREET_NO,
		SUFX_TLDSC,
		ZIP_NM,
		LST_UPD_ID,
		LST_UPD_TS,
		ZIP_SFX_NO,
		ARCASS_IND,
		EMAIL_ADDR,
		IBMSNAP_OPERATION,
		IBMSNAP_LOGMARKER
	) VALUES (
		x.IDENTIFIER,
		x.AGENCY_NM,
		x.CITY_NM,
		x.FAX_NO,
		x.FIRST_NM,
		x.LAST_NM,
		x.NMPRFX_DSC,
		x.PHONE_NO,
		x.TEL_EXT_NO,
		x.PSTITL_DSC,
		x.SVCPVDRC,
		x.STATE_C,
		x.STREET_NM,
		x.STREET_NO,
		x.SUFX_TLDSC,
		x.ZIP_NM,
		x.LST_UPD_ID,
		x.LST_UPD_TS,
		x.ZIP_SFX_NO,
		x.ARCASS_IND,
		x.EMAIL_ADDR,
		'D',
		current timestamp
	);
END
