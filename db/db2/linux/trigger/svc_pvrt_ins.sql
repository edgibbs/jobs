DROP TRIGGER CWSINT.trg_svc_pvrt_ins;

CREATE TRIGGER CWSINT.trg_svc_pvrt_ins
AFTER INSERT ON CWSINT.SVC_PVRT
REFERENCING NEW AS NROW
FOR EACH ROW MODE DB2SQL
BEGIN ATOMIC
	MERGE INTO CWSRS1.SVC_PVRT tgt USING ( 
		SELECT
		nrow.IDENTIFIER,
		nrow.AGENCY_NM,
		nrow.CITY_NM,
		nrow.FAX_NO,
		nrow.FIRST_NM,
		nrow.LAST_NM,
		nrow.NMPRFX_DSC,
		nrow.PHONE_NO,
		nrow.TEL_EXT_NO,
		nrow.PSTITL_DSC,
		nrow.SVCPVDRC,
		nrow.STATE_C,
		nrow.STREET_NM,
		nrow.STREET_NO,
		nrow.SUFX_TLDSC,
		nrow.ZIP_NM,
		nrow.LST_UPD_ID,
		nrow.LST_UPD_TS,
		nrow.ZIP_SFX_NO,
		nrow.ARCASS_IND,
		nrow.EMAIL_ADDR
		FROM sysibm.sysdummy1
	) X ON (tgt.IDENTIFIER = X.IDENTIFIER)
	WHEN MATCHED THEN UPDATE SET 
		AGENCY_NM = x.AGENCY_NM,
		CITY_NM = x.CITY_NM,
		FAX_NO = x.FAX_NO,
		FIRST_NM = x.FIRST_NM,
		LAST_NM = x.LAST_NM,
		NMPRFX_DSC = x.NMPRFX_DSC,
		PHONE_NO = x.PHONE_NO,
		TEL_EXT_NO = x.TEL_EXT_NO,
		PSTITL_DSC = x.PSTITL_DSC,
		SVCPVDRC = x.SVCPVDRC,
		STATE_C = x.STATE_C,
		STREET_NM = x.STREET_NM,
		STREET_NO = x.STREET_NO,
		SUFX_TLDSC = x.SUFX_TLDSC,
		ZIP_NM = x.ZIP_NM,
		LST_UPD_ID = x.LST_UPD_ID,
		LST_UPD_TS = x.LST_UPD_TS,
		ZIP_SFX_NO = x.ZIP_SFX_NO,
		ARCASS_IND = x.ARCASS_IND,
		EMAIL_ADDR = x.EMAIL_ADDR,
		IBMSNAP_OPERATION = 'I',
		IBMSNAP_LOGMARKER = current timestamp
	WHEN NOT MATCHED THEN INSERT (
		IDENTIFIER,
		AGENCY_NM,
		CITY_NM,
		FAX_NO,
		FIRST_NM,
		LAST_NM,
		NMPRFX_DSC,
		PHONE_NO,
		TEL_EXT_NO,
		PSTITL_DSC,
		SVCPVDRC,
		STATE_C,
		STREET_NM,
		STREET_NO,
		SUFX_TLDSC,
		ZIP_NM,
		LST_UPD_ID,
		LST_UPD_TS,
		ZIP_SFX_NO,
		ARCASS_IND,
		EMAIL_ADDR,
		IBMSNAP_OPERATION,
		IBMSNAP_LOGMARKER
	) VALUES (
		x.IDENTIFIER,
		x.AGENCY_NM,
		x.CITY_NM,
		x.FAX_NO,
		x.FIRST_NM,
		x.LAST_NM,
		x.NMPRFX_DSC,
		x.PHONE_NO,
		x.TEL_EXT_NO,
		x.PSTITL_DSC,
		x.SVCPVDRC,
		x.STATE_C,
		x.STREET_NM,
		x.STREET_NO,
		x.SUFX_TLDSC,
		x.ZIP_NM,
		x.LST_UPD_ID,
		x.LST_UPD_TS,
		x.ZIP_SFX_NO,
		x.ARCASS_IND,
		x.EMAIL_ADDR,
		'I',
		current timestamp
	);
END
